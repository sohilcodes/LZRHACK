CREATE TABLE users (
    id INT AUTO_INCREMENT PRIMARY KEY,
    email VARCHAR(100) UNIQUE NOT NULL,
    password VARCHAR(100) NOT NULL,
    expiry_date DATETIME NOT NULL
);

CREATE TABLE keys (
    id INT AUTO_INCREMENT PRIMARY KEY,
    email VARCHAR(100) NOT NULL,
    key_code VARCHAR(255) UNIQUE NOT NULL,
    expiry_date DATETIME NOT NULL
);
